public class TftpException extends Exception {
    public TftpException() {
    }

    public TftpException(String message) {
        super(message);
    }

    public TftpException(String message, Throwable cause) {
        super(message, cause);
    }

    public TftpException(Throwable cause) {
        super(cause);
    }
}
